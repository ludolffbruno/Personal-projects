# Juntar os dicionários em um só
opcoes_texto = {**impostos, **bancos, **clientes}

def obter_resultado():
    opcao1 = var_opcao1.get()
    opcao2 = var_opcao2.get()
    opcao3 = var_opcao3.get()

    resultado = f"{opcoes_texto.get(opcao1, '')} {opcoes_texto.get(opcao2, '')} {opcoes_texto.get(opcao3, '')}"

    caixa_resultado.delete('1.0', tk.END)
    caixa_resultado.insert(tk.END, resultado)

def copiar_resultado():
    resultado = caixa_resultado.get("1.0", "end-1c")
    pyperclip.copy(resultado)

def zerar_opcoes():
    var_opcao1.set(menu_opcao1['values'][0])
    var_opcao2.set(menu_opcao2['values'][0])
    var_opcao3.set(menu_opcao3['values'][0])
    caixa_resultado.delete('1.0', tk.END)

# Criação da janela principal
janela = tk.Tk()
janela.title("Informações Adicionais - By Mr.Ludolff")
janela.geometry("1024x768")

# Criação do frame para os widgets principais
frame_principal = tk.Frame(janela)
frame_principal.pack(pady=20)

# Criação dos widgets no frame
rotulo_opcoes = tk.Label(frame_principal, text="Selecione três opções:")
rotulo_opcoes.grid(row=0, column=0, columnspan=3, pady=(10, 5))

var_opcao1 = tk.StringVar(value="IMPOSTOS")  # Valor padrão da opção 1
menu_opcao1 = ttk.Combobox(frame_principal, values=list(opcoes_texto.keys())[:18], textvariable=var_opcao1)
menu_opcao1.grid(row=1, column=0, padx=10, pady=5)

var_opcao2 = tk.StringVar(value="BANCOS")  # Valor padrão da opção 2
menu_opcao2 = ttk.Combobox(frame_principal, values=list(opcoes_texto.keys())[18:24], textvariable=var_opcao2)
menu_opcao2.grid(row=2, column=0, padx=10, pady=5)

var_opcao3 = tk.StringVar(value="CLIENTES")  # Valor padrão da opção 3
menu_opcao3 = ttk.Combobox(frame_principal, values=list(opcoes_texto.keys())[24:], textvariable=var_opcao3)
menu_opcao3.grid(row=3, column=0, padx=10, pady=5)

# Adicionando botão "OBTER RESULTADO" e "ZERAR"
botao_resultado = tk.Button(frame_principal, text=" OBTER RESULTADO ", command=obter_resultado)
botao_resultado.grid(row=4, column=0, padx=10, pady=5)

botao_zerar = tk.Button(frame_principal, text=" ZERAR ", command=zerar_opcoes)
botao_zerar.grid(row=5, column=0, padx=10, pady=5)

caixa_resultado = tk.Text(frame_principal, height=10, width=80)
caixa_resultado.grid(row=6, column=0, padx=10, pady=5)

botao_copiar = tk.Button(frame_principal, text=" COPIAR RESULTADO ", command=copiar_resultado)
botao_copiar.grid(row=7, column=0, pady=5)

# Função para centralizar o frame verticalmente
def centralizar_frame_vertical():
    frame_principal.update_idletasks()
    y = 120  #(janela.winfo_height() - frame_principal.winfo_height()) // 2
    frame_principal.grid(pady=y)

# Função para centralizar o frame horizontalmente
def centralizar_frame_horizontal():
    frame_principal.update_idletasks()
    x = (janela.winfo_width() - frame_principal.winfo_width()) // 2
    frame_principal.grid(padx=x)

# Centralizar o frame verticalmente e horizontalmente
centralizar_frame_vertical()
centralizar_frame_horizontal()

# Inicialização da janela
janela.mainloop()

